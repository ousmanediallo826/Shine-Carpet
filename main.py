X_REPEAT = 3  # How many times to tessellate horizontally.
Y_REPEAT = 4  # How many times to tessellate vertically.
  
for i in range(Y_REPEAT):
    print(r'___|___|___|___|' * X_REPEAT)
    print(r'___|___|___|___|' * X_REPEAT)
    print(r'___|___|___|___|' * X_REPEAT)
    print(r'___|___|___|___|' * X_REPEAT)
    print(r'___|___|___|___|' * X_REPEAT)
    print(r'___|___|___|___|' * X_REPEAT)